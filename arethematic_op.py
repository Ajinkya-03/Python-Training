a = 10
b = 5

print(a+b)   #output  --> 15
print(a-b)   #output  --> 5  
print(a*b)   #output  --> 50
print(a/b)   #output  --> 2.0    
print(a%b)   #output  --> 0
print(a**b)  #output  --> 100000
print(a//b)  #output -->  2
print(a==b)  #output --> False
print(a!=b)  #output  --> True
print(a>b)   #output  --> True
print(a<b)   #output  --> False
